public class BuildInFunctions {
    public static void main(String[] args) {
        /**
         * A good developer does not memorize code, 
         * a good developer uses the internet. 
         * Read documentation and find resources.
         */

        double sine = Math.sin(1.2);
        System.out.println("The sin of 1.2 is : " + sine);

        double power = Math.pow(2, 4); //1. base, 2. exponent
        System.out.println("2 to the power of 2 is : " + power);

        double random = Math.random() * 10 ;
        System.out.println("The random value is: " + random);
    }
}
